# string method part 1

name = "AsHoK cHoPaDe"
print(name)

# 1.len() function
print("\nlen function")
print(len(name)) # printing length of name variable

# 2.lower() method
print("\nlower method")
print(name.lower()) # Converts a string into lower case

# 3.upper() method
print("\nupper method")
print(name.upper()) # Converts a string into upper case

# 4.title() method
print("\ntitle method")
print(name.title()) # Converts the first character of each word to upper case

# 5.count() method
print("\ncount method") 
print(name.count("A")) # Returns the number of times a specified value occurs in a string
